    private void afegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_afegirActionPerformed
        // TODO add your handling code here:
        boolean bC = validaCodi();
        boolean bN = validaNom();
        boolean bPl = validaPlataforma();
        boolean bQS = validaQStock();
        boolean bPr = validaPreu();
        String tm = donamTematica();

        if (tm == null) {
            errorTematicaLabel.setText("Camp Obligatori");
        } else {
            errorTematicaLabel.setText("");
        }

        if (categoria.getSelectedIndex() == 0) {
            errorCategoriaLabel.setText("Camp Obligatori");
        } else {
            errorCategoriaLabel.setText("");
        }

        if (bC && bN && bPl && bQS && bPr && tm != null && categoria.getSelectedIndex() != 0) {

            Videojoc vj = new Videojoc();
            int codi = Integer.parseInt(this.codiText.getText());
            int qStock = Integer.parseInt(this.qStockText.getText());
            double preu = Double.parseDouble(this.preuText.getText());
            String cat = String.valueOf(categoria.getSelectedItem());
            String plat = this.plataformaText.getText();

            vj.setCodi(codi);
            vj.setNom(this.nomText.getText());
            vj.setPlataforma(plat);
            vj.setQuantStock(qStock);
            vj.setPreu(preu);
            if (siOfertaCheck.isSelected()) {
                vj.setOferta("Oferta");
            } else {
                vj.setOferta("Sense Oferta");
            }
            vj.setCategoria(cat);
            vj.setTematica(tm);

            bctl.afegirVideojoc(vj);
            //   bctl.setEscritura(oos, mgz);

            codiText.setText("");
            nomText.setText("");
            plataformaText.setText("");
            qStockText.setText("");
            preuText.setText("");
            buttonGroup1.clearSelection();
            categoria.setSelectedIndex(0);
            siOfertaCheck.setSelected(false);
        }

    }//GEN-LAST:event_afegirActionPerformed

