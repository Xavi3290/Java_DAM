/**
 * Valida el codi si esta buit i si el codi esta repetit i el camp es obligatori
 * @return boolean
 */
    private boolean validaCodi() {
        boolean valida = false;
        String text = this.codiText.getText();
        if (bctl.validarBuit(text)) {
            errorCodiLabel.setText("Camp obligatori");
        } else {
            try {

                int codi = Integer.parseInt(text);
                if (bctl.buscarVideojoc(codi) != null) {
                    errorCodiLabel.setText("Codi repetit");

                } else {
                    errorCodiLabel.setText("");
                    valida = true;
                }
            } catch (Exception e) {
                System.out.println(e.toString());
            }
        }
        return valida;
    }
